create table if not exists public.school_subscriptions (
  id uuid primary key default gen_random_uuid(),
  organization_id uuid not null references public.organizations(id) on delete cascade,
  product_id uuid references public.products(id) on delete set null,
  status public.school_subscription_status not null default 'trial',
  plan_name text not null default 'ScholarOS Annual School Access',
  starts_at date not null,
  ends_at date not null,
  seat_limit integer not null default 250 check (seat_limit > 0),
  resource_access text not null default 'full' check (resource_access in ('full','limited')),
  payment_reference text,
  metadata jsonb not null default '{}'::jsonb,
  created_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (organization_id, starts_at, ends_at),
  check (ends_at >= starts_at)
);
create index if not exists school_subscriptions_org_status_idx on public.school_subscriptions(organization_id,status,ends_at desc);

create table if not exists public.student_school_memberships (
  id uuid primary key default gen_random_uuid(),
  organization_id uuid not null references public.organizations(id) on delete cascade,
  student_id uuid not null references public.profiles(id) on delete cascade,
  academic_year text not null,
  grade integer not null check (grade between 8 and 12),
  section text,
  board text not null,
  tracks text[] not null default '{}',
  status public.student_membership_status not null default 'active',
  promotion_locked boolean not null default false,
  revoked_at timestamptz,
  revoked_by uuid references public.profiles(id) on delete set null,
  promoted_at timestamptz,
  promoted_by uuid references public.profiles(id) on delete set null,
  parent_name text,
  parent_phone text,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique(organization_id,student_id,academic_year)
);
create index if not exists student_memberships_org_year_idx on public.student_school_memberships(organization_id,academic_year,status,grade);
create index if not exists student_memberships_student_idx on public.student_school_memberships(student_id,status);

create table if not exists public.academic_resources (
  id uuid primary key default gen_random_uuid(),
  title text not null,
  kind public.academic_resource_kind not null,
  access_label public.resource_access_label not null default 'included',
  subscription_required boolean not null default true,
  board text,
  grade_min integer not null default 8 check (grade_min between 8 and 12),
  grade_max integer not null default 12 check (grade_max between 8 and 12 and grade_max >= grade_min),
  required_track text,
  exam_type text,
  subject text,
  source_year integer,
  question_paper_id uuid references public.question_papers(id) on delete set null,
  content_url text,
  is_active boolean not null default true,
  metadata jsonb not null default '{}'::jsonb,
  created_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
create index if not exists academic_resources_eligibility_idx on public.academic_resources(kind,board,grade_min,grade_max,required_track,is_active);

create table if not exists public.student_promotion_events (
